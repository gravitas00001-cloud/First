def env_flag(name, default=False):
    value = config(name, default=None)
    if value is None:
        return default

    normalized = str(value).strip().lower()
    if normalized in TRUTHY_ENV_VALUES:
        return True
    if normalized in FALSY_ENV_VALUES:
        return False

    return default


def env_list(name, default=None):
    value = config(name, default="")
    if value is None:
        return list(default or [])

    parsed_values = [item.strip() for item in str(value).split(",") if item.strip()]
    if parsed_values:
        return parsed_values

    return list(default or [])


def env_int(name, default=0):
    return config(name, cast=int, default=default)


def normalize_origin(value):
    item = str(value).strip()
    if not item:
        return None

    parsed = urlsplit(item)
    if not parsed.scheme or not parsed.netloc:
        return None

    return f"{parsed.scheme}://{parsed.netloc}"


def normalize_host(value):
    item = str(value).strip()
    if not item:
        return None

    parsed = urlsplit(item if "://" in item else f"https://{item}")
    return parsed.hostname


def extend_unique(values, candidate):
    if candidate and candidate not in values:
        values.append(candidate)


